    private void sancionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sancionarActionPerformed
        // TODO add your handling code here:
        if ("Moderador".equals(ventanaPrincipal.foro_.dameRol(ventanaPrincipal.user)))
        {
            Moderador m = ventanaPrincipal.foro_.obtenerModerador(ventanaPrincipal.user.getNick());
            m.sancionar(forero_);
            ventanaListaForeros.refrescarForeros();
        }
        else
        {
            Administrador a = ventanaPrincipal.foro_.obtenerAdministrador(ventanaPrincipal.user.getNick());
            a.sancionar(forero_);
            ventanaListaForeros.refrescarForeros();
        }
           
        
        labelVecesSancionado.setText(Integer.toString(forero_.getVecesSancionado())); 
        if(forero_.getSancionado())
        {
            labelSancionado.setText("Si");
        }
        else
        {
            labelSancionado.setText("No");
        }
        
        sancionar.setVisible(false);
        levantarSancion.setVisible(true);
        
        try {
            ventanaPrincipal.foro_.actualizarEnMemoria();
        } catch (IOException ex) {
            Logger.getLogger(ventanaForero.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_sancionarActionPerformed

