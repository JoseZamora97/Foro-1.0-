    public ventanaForero(Forero fore_) {
        initComponents();
        setLocationRelativeTo(null);
        forero_ = fore_;
        labelError.setVisible(false);
        sancionar.setVisible(false);
        levantarSancion.setVisible(false);
        ficheroCreado.setVisible(false);
        labelNick.setText(forero_.getNick());
        if(!forero_.getNombre().isEmpty())
        {
            labelNombre.setText(forero_.getNombre());
        }else{
            labelNombre.setText("<unknown>");
        }
        
        if(!forero_.getApellidos().isEmpty())
        {
            labelApellidos.setText(forero_.getApellidos());
        }else{
            labelApellidos.setText("<unknown>");
        }
        
        if(forero_.CategoriaMasFrecuente() != null)
        {
            labelCategoriaFrecuente.setText(forero_.CategoriaMasFrecuente().getNombre());
        }else{
            labelCategoriaFrecuente.setText("<none>");
        }
        
        labelNumeroMensajes.setText(Integer.toString(forero_.getNumMensajes()));
        
        labelRol.setText(ventanaPrincipal.foro_.dameRol(forero_));
        
        if(!forero_.getTelefono().isEmpty())
        {
            labelTelefono.setText(forero_.getTelefono());
        }else{
            labelTelefono.setText("<unknown>");
        }
        
        if(forero_.getSancionado())
        {
            labelSancionado.setText("Si");
        }else{
            labelSancionado.setText("No");
        }
        
        labelVecesSancionado.setText(Integer.toString(forero_.getVecesSancionado()));
        
        if("Administrador".equals(ventanaPrincipal.foro_.dameRol(ventanaPrincipal.user)))
        {
            if("Administrador".equals(ventanaPrincipal.foro_.dameRol(forero_)))
            {
                ascenderModeradorBut.setVisible(false);
                degradarModeradorBut.setVisible(false);
            }else if("Moderador".equals(ventanaPrincipal.foro_.dameRol(forero_)))
            {
                ascenderModeradorBut.setVisible(true);
                degradarModeradorBut.setVisible(true);
            }else{
                ascenderModeradorBut.setVisible(true);
                degradarModeradorBut.setVisible(false);
                if (forero_.getSancionado())
                    levantarSancion.setVisible(true);
                else
                    sancionar.setVisible(true);
            }
        }
        else if ("Moderador".equals(ventanaPrincipal.foro_.dameRol(ventanaPrincipal.user)))
        {
            if("Administrador".equals(ventanaPrincipal.foro_.dameRol(forero_)))
            {
                ascenderModeradorBut.setVisible(false);
                degradarModeradorBut.setVisible(false);
            }else if("Moderador".equals(ventanaPrincipal.foro_.dameRol(forero_)))
            {
                ascenderModeradorBut.setVisible(false);
                degradarModeradorBut.setVisible(false);
                sancionar.setVisible(false);
                levantarSancion.setVisible(false);
            }else{
                ascenderModeradorBut.setVisible(false);
                degradarModeradorBut.setVisible(false);
                if (forero_.getSancionado())
                    levantarSancion.setVisible(true);
                else
                    sancionar.setVisible(true);
            }

        }else{
            ascenderModeradorBut.setVisible(false);
            degradarModeradorBut.setVisible(false);
            jTextFieldFichero.setVisible(false);
            jButtonCrearFichero.setVisible(false);
            jLabel9.setVisible(false);

        }
    }

