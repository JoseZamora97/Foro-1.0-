    private void ListaModMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListaModMouseClicked
        moderador_ = ListaMod.getSelectedValue();
        if(!moderador_.getForerosSancionados().isEmpty() && !moderador_.getForerosSancionadosHist().isEmpty())
        {
            this.dispose();
            new ventanaModerador(moderador_).setVisible(true);
        }
        else
        {
            new ventanaAvisoNoSancionados(this,true).setVisible(true);
        }
    }//GEN-LAST:event_ListaModMouseClicked

