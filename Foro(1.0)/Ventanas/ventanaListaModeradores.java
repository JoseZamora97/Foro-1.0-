    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        dlMod.clear();
        if (jToggleButton1.isSelected()){
            if(ventanaPrincipal.foro_.getModeradoresOrdenado().isEmpty() && ventanaPrincipal.foro_.getAdministradores().isEmpty()){
                new ventanaAvisoNoForeros(this, true).setVisible(true);
                this.dispose();
                
            }else{

                ArrayList<Moderador> modOrdRev = new ArrayList();
                modOrdRev.clear();
                modOrdRev.addAll(ventanaPrincipal.foro_.getModeradores());
                modOrdRev.addAll(ventanaPrincipal.foro_.getAdministradores());
                modOrdRev.sort(new NumSancionesModerador());
                
                Collections.reverse(modOrdRev);
                for (Moderador m : modOrdRev){
                    dlMod.addElement(m);
                }
            }
        }else
        {
            refrescarModeradores();
        }
                
    }//GEN-LAST:event_jToggleButton1ActionPerformed

