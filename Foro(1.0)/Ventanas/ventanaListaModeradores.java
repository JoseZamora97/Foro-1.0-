    private void refrescarModeradores() {
        dlMod.clear();
        if(ventanaPrincipal.foro_.getModeradores().isEmpty() && ventanaPrincipal.foro_.getAdministradores().isEmpty())
        {
            new ventanaAvisoNoForeros(this, true).setVisible(true);
            this.dispose();
            
        }else{
            ArrayList<Moderador> listr = new ArrayList();
            listr.clear();
            listr.addAll(ventanaPrincipal.foro_.getModeradores());
            listr.addAll(ventanaPrincipal.foro_.getAdministradores());
            Collections.sort(listr);
            
            
            for (Moderador m : listr)
            {
                dlMod.addElement(m);
            }
        }
    }
